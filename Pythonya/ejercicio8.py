#Calcular el sueldo mensual de un operario conociendo 
#la cantidad de horas trabajadas y el valor por hora.
horas_trabajadas = int(input("Ingrese horas trabajadas: "))
print(horas_trabajadas)
valor_hora = int(input("Valor de hora trabajada: "))
print(valor_hora)


sueldo_mensual = horas_trabajadas * valor_hora

print("El sueldo mensual es: ")
print(sueldo_mensual)
